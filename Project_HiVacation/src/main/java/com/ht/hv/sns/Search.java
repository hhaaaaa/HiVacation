package com.ht.hv.sns;

public class Search {  
	private String what;
	private String search;
	
	public Search() {
	}

	public Search(String what, String search) {
		super();
		this.what = what;
		this.search = search;
	}

	public String getWhat() {
		return what;
	}

	public void setWhat(String what) {
		this.what = what;
	}

	public String getSearch() {
		return search;
	}

	public void setSearch(String search) {
		this.search = search;
	}
	
	
}
